<h3>Singleton</h3>
<h4>Definição:</h4>
<p>Cria apenas uma instancia global daquele objeto.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Singleton" target="_blank">Design Pattern Singleton</a></p>

<p>Tornando o construtor privado, só é possivel criar o Objeto Estaticamente, onde se já existir este objeto criado anteriormente é retornado ele novamente.</p>

