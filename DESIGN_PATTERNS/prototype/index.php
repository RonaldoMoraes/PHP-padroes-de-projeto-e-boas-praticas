<h3>Prototype</h3>
<h4>Definição:</h4>
<p>Permite a criação de novos objetos tendo como base um modelo original.</p>

<p>Ao criar muitos objetos com NEW, pode consumir muita memória.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Prototype" target="_blank">Design Pattern Prototype</a></p>

<p>Onde irá ser feito um Clone do objeto base.</p>

