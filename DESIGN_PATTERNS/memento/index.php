<h3>Memento</h3>
<h4>Definição:</h4>
<p>Quando se quer ter um histórico das mudanças desse objeto, ver historico, voltar para algum ponto desejado.</p>

<p>O Memento guarda uma lista com cópias desse Objeto que se deseja ter o Histórico.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Memento_(inform%C3%A1tica)" target="_blank">Design Pattern Memento</a></p>

