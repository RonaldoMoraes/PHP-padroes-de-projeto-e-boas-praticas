<h3>Proxy</h3>
<h4>Definição:</h4>
<p>Quando se quer adicionar uma camada de segurança a algum objeto sem alterar ele.</p>

<p>Passando este objeto para o Proxy, ele irá fazer as verificações de segurança antes de chamar os métodos da Classe desejada.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Proxy_(padrões_de_projeto)" target="_blank">Design Pattern Proxy</a></p>

