<h3>Composite</h3>
<h4>Definição:</h4>
<p>Quando se quer ler uma Objeto ou Conjuntos desse Objeto de uma só forma.</p>

<p>Um menu de que o Item, ou podem existir outros Menus dentro do Menu com seus Itens, assim o Composite entra em cada nó dessa arvore de maneira transparente.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Composite" target="_blank">Design Pattern Composite</a></p>

