<h3>Façade</h3>
<h4>Definição:</h4>
<p>Um objeto que provê de forma simplicada uma abstração de um código maior.</p>

<p>Se para ligar um Computador, é necessário instanciar CPU, HD, Memoria, e chamar seus métodos, isso se torna bastante complicado.</p>

<p>O ideal é usar o <a href="https://pt.wikipedia.org/wiki/Façade" target="_blank">Design Pattern Façade</a></p>

<p>Onde o Facade irá abstrair toda essa logica, e irá apenas ter uma função X, com seus parametros, e fazendo todo o processo de instanciar os objetos e chamar seus métodos.</p>

